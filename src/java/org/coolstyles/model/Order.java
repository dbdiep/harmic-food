package org.coolstyles.model;

/**
 *
 * @author Admin
 */
public class Order {
    public static String PENDING_STATUS = "pending";
    public static String FINISHED_STATUS = "finished";
    public static int CODE_LENGHT = 8;
    
    private int id;
    private String name;
    private String description;
    private String status;

    public Order(int id, String name, String description, String status) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
